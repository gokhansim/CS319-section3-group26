package Model;

public class GameBody {
	private int x;
	private int y;

	public GameBody( int x, int y){
		this.x = x;
		this.y = y;
	}

	public int getX(){
		return this.y;
	}

	public int getY() {
		return this.y;
	}

	public void setX( int x){
		this.x = x;
	}

	public void setY( int y) {
		this.y = y;
	}
}
